def pack_elements_ffd(elements, capacity):
    """
    Упаковывает элементы в ящики с использованием алгоритма First Fit Decreasing (FFD).

    Алгоритм:
    1. Сортируем элементы в порядке убывания.
    2. Создаем список ящиков (изначально пустой).
    3. Для каждого элемента:
        - Если элемент помещается в последний ящик, добавляем его туда.
        - Иначе создаем новый ящик и кладем элемент в него.

    Аргументы:
        elements: список элементов для упаковки.
        capacity: вместимость каждого ящика.

    Возвращает:
        Список ящиков, где каждый ящик представлен списком элементов.
    """

    # 1. Сортировка элементов в порядке убывания:
    elements.sort(reverse=True)

    # 2. Инициализация списка ящиков:
    boxes = []

    # 3. Цикл по элементам:
    for element in elements:
        # 3.1. Проверка, можно ли добавить элемент в последний ящик:
        if boxes and boxes[-1] + element <= capacity:
            boxes[-1] += element  # Добавление элемента в последний ящик
        else:
            # 3.2. Если элемент не помещается, создаем новый ящик:
            boxes.append(element)  # Создание нового ящика с одним элементом

    return boxes

# Пример использования:
elements = [4, 4, 6, 6, 1, 8, 9, 1, 4, 2, 1]
capacity = 10
boxes = pack_elements_ffd(elements, capacity)

print(f"Элементы: {elements}")
print(f"Вместимость: {capacity}")
print(f"Ящики: {boxes}")
print(f"Количество ящиков: {len(boxes)}")