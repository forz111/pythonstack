def subset_sum(numbers, target):  # Определение функции subset_sum с двумя аргументами: список чисел и целевая сумма.

  n = len(numbers)  # Получение длины списка чисел.
  dp = [[False] * (target + 1) for _ in range(n + 1)]  # Создание двумерного массива размером (n+1) x (target+1), заполненного False.

  for i in range(n + 1):  # Цикл по всем элементам от 0 до n.
    dp[i][0] = True  # Установка первого столбца в True, так как сумма 0 может быть достигнута пустым подмножеством.

  for i in range(1, n + 1):  # Цикл по всем элементам от 1 до n.
    for j in range(1, target + 1):  # Цикл по всем элементам от 1 до target.
      if numbers[i - 1] <= j:  # Если текущее число меньше или равно текущей целевой сумме.
        dp[i][j] = dp[i - 1][j - numbers[i - 1]] or dp[i - 1][j]  # Обновление значения в таблице dp.
      else:
        dp[i][j] = dp[i - 1][j]  # Если текущее число больше текущей целевой суммы, то значение в таблице dp остается прежним.

  if dp[n][target]:  # Если в последней ячейке таблицы dp стоит True, то существует подмножество с суммой равной target.
    subset = []  # Инициализация списка для хранения подмножества.
    i = n  # Начало с последнего элемента.
    j = target  # Начало с целевой суммы.
    while i > 0 and j > 0:  # Пока не достигнуты начальные значения.
      if not dp[i - 1][j]:  # Если значение в ячейке выше равно False.
        subset.append(numbers[i - 1])  # Добавление текущего числа в подмножество.
        j -= numbers[i - 1]  # Уменьшение целевой суммы на текущее число.
      i -= 1  # Переход к следующему числу.
    return subset  # Возвращение подмножества.
  else:
    return None  # Если в последней ячейке таблицы dp стоит False, то подмножества с суммой равной target не существует.

numbers = [3, 9, 8, 4, 5, 7, 10]  # Инициализация списка чисел.
target = 23  # Установка целевой суммы.

subset = subset_sum(numbers, target)  # Вызов функции subset_sum и получение подмножества.

if subset:  # Если подмножество существует.
  print("Подмножество с суммой", target, ":", subset)  # Вывод подмножества.
else:
  print("Не существует подмножества с суммой", target)  # Вывод сообщения о том, что подмножества не существует.